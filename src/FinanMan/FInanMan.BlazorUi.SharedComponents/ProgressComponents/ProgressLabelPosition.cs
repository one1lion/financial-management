namespace FinanMan.BlazorUi.SharedComponents.ProgressComponents;

/// <summary>
/// The position of the label in the progress bar
/// </summary>
public enum ProgressLabelPosition
{
    /// <summary>
    /// The label will not be displayed
    /// </summary>
    Off,
    /// <summary>
    /// The label will be displayed at the start of the progress bar
    /// </summary>
    Start,
    /// <summary>
    /// The label will be displayed in the center of the progress bar
    /// </summary>
    Center,
    /// <summary>
    /// The label will be displayed at the end of the progress bar
    /// </summary>
    End
}
